// ekhane \n holo escape sequence. \n mane holo new line.
// \t mane holo tab space. 
// \b mane holo backspace. 
// \r mane holo carriage return. 
//  // mane holo backslash. 
// \' mane holo single quote. 
// \" mane holo double quote.
public class escapes {
    public static void main(String[] args) {
        System.out.println("Atul\nPaul");
        System.out.println("Atul\tpaul");
        System.out.println("My name is \"Atul\"");
        
    }
}
